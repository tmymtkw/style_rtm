class U2NETP(nn.Module):

    def __init__(self,in_ch=3,out_ch=1):
        super(U2NETP,self).__init__()

        self.stage1 = RSU7(in_ch,16,64)
        self.pool12 = nn.MaxPool2d(2,stride=2,ceil_mode=True)

        self.stage2 = RSU6(64,16,64)
        self.pool23 = nn.MaxPool2d(2,stride=2,ceil_mode=True)

        self.stage3 = RSU5(64,16,64)
        self.pool34 = nn.MaxPool2d(2,stride=2,ceil_mode=True)

        self.stage4 = RSU4(64,16,64)
        self.pool45 = nn.MaxPool2d(2,stride=2,ceil_mode=True)

        self.stage5 = RSU4F(64,16,64)
        self.pool56 = nn.MaxPool2d(2,stride=2,ceil_mode=True)

        self.stage6 = RSU4F(64,16,64)

        # decoder
        self.stage5d = RSU4F(128,16,64)
        self.stage4d = RSU4(128,16,64)
        self.stage3d = RSU5(128,16,64)
        self.stage2d = RSU6(128,16,64)
        self.stage1d = RSU7(128,16,64)

        self.side1 = nn.Conv2d(64,out_ch,3,padding=1)
        self.side2 = nn.Conv2d(64,out_ch,3,padding=1)
        self.side3 = nn.Conv2d(64,out_ch,3,padding=1)
        self.side4 = nn.Conv2d(64,out_ch,3,padding=1)
        self.side5 = nn.Conv2d(64,out_ch,3,padding=1)
        self.side6 = nn.Conv2d(64,out_ch,3,padding=1)

        self.outconv = nn.Conv2d(6*out_ch,out_ch,1)

    def forward(self,x):

        hx = x

        #stage 1
        hx1 = self.stage1(hx)
        hx = self.pool12(hx1)

        #stage 2
        hx2 = self.stage2(hx)
        hx = self.pool23(hx2)

        #stage 3
        hx3 = self.stage3(hx)
        hx = self.pool34(hx3)

        #stage 4
        hx4 = self.stage4(hx)
        hx = self.pool45(hx4)

        #stage 5
        hx5 = self.stage5(hx)
        hx = self.pool56(hx5)

        #stage 6
        hx6 = self.stage6(hx)
        hx6up = _upsample_like(hx6,hx5)

        #decoder
        hx5d = self.stage5d(torch.cat((hx6up,hx5),1))
        hx5dup = _upsample_like(hx5d,hx4)

        hx4d = self.stage4d(torch.cat((hx5dup,hx4),1))
        hx4dup = _upsample_like(hx4d,hx3)

        hx3d = self.stage3d(torch.cat((hx4dup,hx3),1))
        hx3dup = _upsample_like(hx3d,hx2)

        hx2d = self.stage2d(torch.cat((hx3dup,hx2),1))
        hx2dup = _upsample_like(hx2d,hx1)

        hx1d = self.stage1d(torch.cat((hx2dup,hx1),1))


        #side output
        d1 = self.side1(hx1d)

        d2 = self.side2(hx2d)
        d2 = _upsample_like(d2,d1)

        d3 = self.side3(hx3d)
        d3 = _upsample_like(d3,d1)

        d4 = self.side4(hx4d)
        d4 = _upsample_like(d4,d1)

        d5 = self.side5(hx5d)
        d5 = _upsample_like(d5,d1)

        d6 = self.side6(hx6)
        d6 = _upsample_like(d6,d1)

        d0 = self.outconv(torch.cat((d1,d2,d3,d4,d5,d6),1))

        return F.sigmoid(d0), F.sigmoid(d1), F.sigmoid(d2), F.sigmoid(d3), F.sigmoid(d4), F.sigmoid(d5), F.sigmoid(d6)

class EdgeConv(nn.Module):
    def __init__(self):
        super().__init__()
        self.kernel_horizon = torch.tensor([[-1, 0, 1], 
                                            [-1, 0, 1], 
                                            [-1, 0, 1]], 
                                            device=torch.device("cuda"),
                                            dtype=torch.float32).view(1, 1, 3, 3)
        self.kernel_horizon.requires_grad = False
        self.kernel_vertical = torch.tensor([[1, 1, 1], 
                                             [0, 0, 0], 
                                             [-1, -1, -1]], 
                                             device=torch.device("cuda"),
                                             dtype=torch.float32).view(1, 1, 3, 3)
        self.kernel_vertical.requires_grad = False
        self.gray_kernel = torch.tensor([0.299, 0.587, 0.114], 
                                        device=torch.device("cuda"),
                                        dtype=torch.float32).reshape(3, 1, 1)  # color -> gray kernel
        self.gray_kernel.requires_grad = False

        self.layer1 = nn.Sequential(nn.Conv2d(in_channels=1, out_channels=17, kernel_size=7, stride=4, padding=2, bias=False),
                                    nn.BatchNorm2d(num_features=17),
                                    nn.ReLU())
        self.layer2 = nn.Sequential(nn.Conv2d(in_channels=17, out_channels=17, kernel_size=5, stride=2, padding=2, bias=False),
                                    nn.BatchNorm2d(num_features=17),
                                    nn.ReLU())

    def getedge(self, color):
        gray = torch.sum(color * self.gray_kernel, dim=1, keepdim=True)  # grayscale image [B, 1, H, W]
        edge = nn.functional.conv2d(gray, self.kernel_horizon, padding=1) + nn.functional.conv2d(gray, self.kernel_vertical, padding=1)

        return edge
    
    def forward(self, x) -> Tuple: 
        x_edge = self.getedge(x)
        edge1 = self.layer1(x_edge)
        edge2 = self.layer2(edge1)
        return (edge1, edge2)

class ConvBlock(nn.Module):
    def __init__(self, channels, groups=17):
        super().__init__()
        self.groups = groups
        self.channels = channels
        self.conv = nn.Conv2d(self.channels, self.channels, 3, 1, 1, groups=self.groups)
        self.norm = nn.BatchNorm2d(num_features=self.groups)
        self.relu = nn.ReLU()

    def forward(self, x):
        x = self.conv(x)
        x = self.norm(x)
        x = self.relu(x)
        return x

class DeconvBlock(nn.Module):
    def __init__(self, channels, groups=17):
        super().__init__()
        self.groups = groups
        self.channels = channels
        self.conv = nn.ConvTranspose2d(self.channels, self.channels, 3, 1, 1, groups=self.groups)
        self.norm = nn.BatchNorm2d(num_features=self.groups)
        self.relu = nn.ReLU()

    def forward(self, x):
        x = self.conv(x)
        x = self.norm(x)
        x = self.relu(x)
        return x

